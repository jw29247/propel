import type { PropelConfig } from "../config/config.js";

export function setPluginEnabledInConfig(
  config: PropelConfig,
  pluginId: string,
  enabled: boolean,
): PropelConfig {
  return {
    ...config,
    plugins: {
      ...config.plugins,
      entries: {
        ...config.plugins?.entries,
        [pluginId]: {
          ...(config.plugins?.entries?.[pluginId] as object | undefined),
          enabled,
        },
      },
    },
  };
}
